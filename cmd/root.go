package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "floppy",
	Short: "A distributed file storage solution for a personal network",
	Long: `Floppy is a CLI Application that allows for distributed file storage. It
	allows users to upload, download, and manage files across multiple computers on a single network`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

